"""6. Definir una funció generar_n_caracters() que tomi un enter n i retorni el caràcter multiplicat per n. Per exemple:
    generar_n_caracters(5, "x") hauria de tornar "xxxxx"."""


def generar_n_caracters(n, caracter):
    return caracter * n


print(generar_n_caracters(2, "claudia"))
print(generar_n_caracters(4, "diego"))
print(generar_n_caracters(6, "sims3"))
